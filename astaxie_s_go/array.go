package main

func main() {

	var arr [10]int
	arr[0] = 100
	arr[1] = 200

	/**
	 * 长度是数组的一部分
	 * [3]int 和 [4]int 是不同的类型
	 * 数组不能改变长度
	 */

	// 可以省略长度而采用`...`的方式, Go会自动根据元素个数来计算长度
	c := [...]int{4, 5, 6}

	// 嵌套数组
	doubleArray := [...][4]int{[4]int{1, 2, 3, 4}, [4]int{5, 6, 7, 8}}
	// 简化版
	easyArray := [...][4]int{{1, 2, 3, 4}, {5, 6, 7, 8}}

}
